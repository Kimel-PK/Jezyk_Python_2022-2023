B = set([0, 3, 4, 8, 17, 20])

print ("A = ", end = "")
print (A)
print ("B = ", end = "")
print (B)

print ("A ∩ B = ", end = "")
print (A & B)
print ("A U B = ", end = "")
print (A | B)

